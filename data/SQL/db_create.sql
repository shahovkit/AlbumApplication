CREATE TABLE album
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    picture VARCHAR(200),
    artist VARCHAR(100) NOT NULL,
    title VARCHAR(100) NOT NULL
);

CREATE TABLE role
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    parent_id INT(11),
    roleId VARCHAR(255),
    CONSTRAINT FK_57698A6A727ACA70 FOREIGN KEY (parent_id) REFERENCES role (id)
);

CREATE INDEX IDX_57698A6A727ACA70 ON role (parent_id);

CREATE UNIQUE INDEX UNIQ_57698A6AB8C2FD88 ON role (roleId);

CREATE TABLE user_role_linker
(
    user_id INT(11) NOT NULL,
    role_id INT(11) NOT NULL,
    CONSTRAINT `PRIMARY` PRIMARY KEY (user_id, role_id),
    CONSTRAINT FK_61117899A76ED395 FOREIGN KEY (user_id) REFERENCES users (id),
    CONSTRAINT FK_61117899D60322AC FOREIGN KEY (role_id) REFERENCES role (id)
);

CREATE INDEX IDX_61117899A76ED395 ON user_role_linker (user_id);

CREATE INDEX IDX_61117899D60322AC ON user_role_linker (role_id);

CREATE TABLE users
(
    id INT(11) PRIMARY KEY NOT NULL AUTO_INCREMENT,
    username VARCHAR(255),
    email VARCHAR(255) NOT NULL,
    displayName VARCHAR(50),
    password VARCHAR(128) NOT NULL
);

CREATE UNIQUE INDEX UNIQ_1483A5E9E7927C74 ON users (email);

CREATE UNIQUE INDEX UNIQ_1483A5E9F85E0677 ON users (username);